n=input('digite algo:')
print(n,'é um numero?:',n.isnumeric())
print(n,'é uma letra?:',n.isalpha())
print(n, 'é um numero decimal?', n.isdecimal())
print(n, 'está capitalizado?', n.istitle())
print(n,'é composto somente de espaços?', n.isspace())
print(n,'é composto somente de letras minusculas?', n.islower())
print(n,'é composto somente de letras maisculas?', n.isupper())


